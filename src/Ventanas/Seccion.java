    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        if (!jTextFieldSeccion.getText().equals("")) {
            String seccion = jTextFieldSeccion.getText();
            try {

                Connection cn = Conexion.Conexion();
                PreparedStatement pre = cn.prepareStatement("INSERT INTO seccion (id,Seccion) value(?,?)");
                pre.setInt(1, 0);
                pre.setString(2, seccion);
                pre.executeUpdate();
                jTextFieldSeccion.setText("");
                dispose();
                Producto.llenarSeccion();
            } catch (SQLException e) {
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_jButtonAgregarActionPerformed

