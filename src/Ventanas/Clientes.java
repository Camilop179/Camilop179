    boolean clienteNuevo(String Cedula) {
        boolean cliente = false;
        try (Connection cn = Conexion.Conexion()) {
            PreparedStatement ps = cn.prepareStatement("select nombres from clientes where cedula=?");
            ps.setString(1, Cedula);
            ResultSet rs = ps.executeQuery();
            if(rs.next()) {
                cliente = true;
            }
            cn.close();
        } catch (SQLException ex) {
            System.err.println("Error agregar usuario: " + ex);
            JOptionPane.showMessageDialog(this, "Error Comprobar Usuatio: \n" + ex);
        }
        return cliente;
    }

    void eliminarCliente(int id) {
        
        try(Connection cn = Conexion.Conexion()){
            PreparedStatement ps = cn.prepareStatement("DELETE FROM clientes WHERE idclientes = ?");
            ps.setInt(1, id);
            ps.execute();
            cn.close();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Error eliminar cliente:\n"+ex);
        }
    }
    public static void limpiar() {
        DefaultTableModel tabla = (DefaultTableModel) jTableUsuarios.getModel();
        for (int i = 0; i < jTableUsuarios.getRowCount(); i++) {
            tabla.removeRow(i);
            i--;

        }
    }

