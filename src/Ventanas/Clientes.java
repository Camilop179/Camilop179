    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextFieldNombre.getText();
        String cedula = jTextFieldCedula.getText();
        String celular = jTextFieldCelular.getText();
        if (!clienteNuevo(cedula)) {
            try (Connection cn = Conexion.Conexion()) {
                PreparedStatement ps = cn.prepareStatement("insert into clientes(idclientes,nombres,cedula,celular) values (?,?,?,?)");
                ps.setInt(1, 0);
                ps.setString(2, nombre);
                ps.setString(3, cedula);
                ps.setString(4, celular);
                ps.execute();
                cn.close();
            } catch (SQLException ex) {

                JOptionPane.showMessageDialog(this, "Error agregar Usuatio: \n" + ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Cliente ya registrado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

