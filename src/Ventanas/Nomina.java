    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int nro = Integer.parseInt(jLabelNro.getText());

        try {
            Connection cn = Conexion.Conexion();
            PreparedStatement pr = cn.prepareStatement("select max(NroNomina) from nomina");
            ResultSet rs = pr.executeQuery();
            while (rs.next()) {

                int nur = rs.getInt(1);

                if (nur <= nro) {

                    jButton4.setVisible(false);
                    jTextFieldCedula.setEditable(true);
                    jTextFieldConceptoDed.setEditable(true);

                    jButton1.setVisible(true);
                    limpiar();
                    nroNomina();
                } else {
                    nro++;
                    jLabelNro.setText("" + nro);
                    limpiar();
                    buscarDetalle(String.valueOf(nro));
                    buscarNomina(String.valueOf(nro));
                }
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

