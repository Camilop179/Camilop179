    void buscarEmpleado() {
        try (Connection cn = Conexion.Conexion()) {
            PreparedStatement ps = cn.prepareStatement("select Nombre from empleados where Cedula= ?");
            ps.setString(1, jTextFieldCedula.getText().trim());
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                jLabel6.setText(rs.getString(1));
            } else {
                new BuscarEmpleado(this, true).setVisible(true);
            }
            cn.close();
        } catch (SQLException ex) {

        }
    }
    
    void buscarNomina(String nro) {
        try (Connection cn = Conexion.Conexion()) {
            PreparedStatement ps = cn.prepareStatement("select * from nomina where NroNomina = ?");
            ps.setString(1, nro);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                jTextFieldCedula.setText(rs.getString(3));
                buscarEmpleado();
                jLabelTotal.setText(rs.getString(4));
                jDateChooser1.setDate(new java.util.Date(rs.getDate(5).getTime()));
                jDateChooser2.setDate(new java.util.Date(rs.getDate(6).getTime()));
            }
            cn.close();
        } catch (SQLException ex) {
            System.err.println(ex);
        }
    }

    void nomina() {
        try (Connection cn = Conexion.Conexion()) {
            PreparedStatement ps = cn.prepareStatement("INSERT INTO nomina (id,NroNomina,CC,Total,fecha1,fecha2) values(?,?,?,?,?,?)");
            ps.setInt(1, 0);;
            ps.setInt(2, Integer.parseInt(jLabelNro.getText().trim()));
            ps.setString(3, jTextFieldCedula.getText());
            ps.setDouble(4, Double.parseDouble(jLabelTotal.getText().replace(",", "")));
            ps.setDate(5, new Date(jDateChooser1.getDate().getTime()));
            ps.setDate(6, new Date(jDateChooser2.getDate().getTime()));
            ps.execute();
            cn.close();
        } catch (SQLException ex) {
            System.err.println("Error un subir nomina: " + ex);
            JOptionPane.showMessageDialog(this, "Error, No se puedo Subir nomina: " + ex);
        }
    }

    void detalleNomina() {
        try (Connection cn = Conexion.Conexion()) {
            PreparedStatement ps = cn.prepareStatement("INSERT INTO detallenomina (id,NroNomina,concepto,descripcion,cant,devengo,deducido,total) values(?,?,?,?,?,?,?,?)");
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                ps.setInt(1, 0);
                ps.setString(2, jLabelNro.getText());
                ps.setString(3, jTable1.getValueAt(i, 0).toString());
                ps.setString(4, jTable1.getValueAt(i, 1).toString());
                ps.setString(5, jTable1.getValueAt(i, 2).toString());
                ps.setString(6, jTable1.getValueAt(i, 3).toString());
                ps.setString(7, jTable1.getValueAt(i, 4).toString());
                ps.setString(8, jTable1.getValueAt(i, 5).toString());
                ps.execute();
            }
            cn.close();
        } catch (SQLException ex) {
            System.err.println("Error un subir nomina: " + ex);
            JOptionPane.showMessageDialog(this, "Error, No se puedo Subir nomina: " + ex);
        }
    }

    void limpiar() {
        DefaultTableModel tf = (DefaultTableModel) jTable1.getModel();
        for (int i = jTable1.getRowCount() - 1; i >= 0; i--) {
            tf.removeRow(i);
        }
        jTextFieldCedula.setText("");
        jLabel6.setText("");
        jTextFieldCantDed.setText("");
        jTextFieldCantDv.setText("");
        jTextFieldConceptoDed.setText("");
        jTextFieldConceptoDeven.setText("");
        jTextFieldDescripDeven.setText("");
        jTextFieldDescripcionDed.setText("");
        jTextFieldValorDed.setText("");
        jTextFieldValorDeven.setText("");
    }

    public void buscarConNomina(String Codigo) {
        try (Connection cn = Conexion.Conexion()) {
            PreparedStatement ps = cn.prepareStatement("select * from connomina where concepto=?");
            ps.setString(1, Codigo);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                if (i == 1) {
                    jTextFieldDescripDeven.setText(rs.getString(3));
                    jTextFieldDescripDeven.requestFocus();
                    i = 0;
                } else if (i == 2) {
                    jTextFieldDescripcionDed.setText(rs.getString(3));
                    jTextFieldDescripcionDed.requestFocus();
                    i = 0;
                }
            } else {
                new connomina().setVisible(true);
            }
            cn.close();
        } catch (SQLException ex) {
            System.err.println("Error al buscar nomina: " + ex);
        }
    }

