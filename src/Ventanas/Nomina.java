    public void buscarDetalle(String NroVenta) {
        limpiar();
        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        String[] datos = new String[6];
        try {
            Connection cn = Conexion.Conexion();
            PreparedStatement pr2 = cn.prepareStatement("select concepto,descripcion,cant,devengo,deducido,total from detallenomina where NroNomina = ?");
            pr2.setString(1, NroVenta);
            ResultSet rs2 = pr2.executeQuery();
            while (rs2.next()) {
                for (int i = 0; i < datos.length; i++) {
                    datos[i] = rs2.getString(i + 1);
                }
                tabla.addRow(datos);

            }
            cn.close();
        } catch (SQLException e) {
            System.err.println(e);
        }
    }

    void nroNomina() {
        int nro_venta = 0;
        try (Connection cn = Conexion.Conexion()) {
            PreparedStatement pr = cn.prepareStatement("select max(NroNomina) from nomina");
            ResultSet rs = pr.executeQuery();

            while (rs.next()) {
                nro_venta = rs.getInt(1);
                nro_venta++;
                jLabelNro.setText("" + nro_venta);
            }
            cn.close();
        } catch (SQLException e) {
            System.err.println(e);
        }
    }

    public final void eventotabla() {
        jTable1.getModel().addTableModelListener((TableModelEvent e) -> {
            if (e.getType() == TableModelEvent.INSERT) {
                int row = e.getLastRow();
                cambiarCant(row);
                total();
            }
            if (e.getType() == TableModelEvent.UPDATE) {
                int columna = e.getColumn();
                int row = e.getLastRow();
                switch (columna) {
                    case 2 -> {
                        cambiarCant(row);
                        total();
                    }
                    case 4 -> {
                        cambiarCant(row);
                        total();
                    }
                    case 3 -> {
                        cambiarCant(row);
                        total();
                    }
                    default -> {
                    }
                }
            }
        });
    }

    public void cambiarCant(int row) {
        int cant = Integer.parseInt(jTable1.getValueAt(row, 2).toString());
        double precio = Double.parseDouble(jTable1.getValueAt(row, 3).toString());

        double precio2 = Double.parseDouble(jTable1.getValueAt(row, 4).toString());
        double total1 = precio * cant + precio2 * cant;

        jTable1.setValueAt(total1, row, 5);

    }

    public void total() {
        double t = 0;
        for (int i = 0; i < jTable1.getRowCount(); i++) {

            t += Double.parseDouble(jTable1.getValueAt(i, 5).toString());

        }
        jLabelTotal.setText(FormatoPesos.formato(t));
    }

