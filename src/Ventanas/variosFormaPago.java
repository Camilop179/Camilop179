    void total() {
        double total = 0;
        double resto;
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            total += Double.parseDouble(jTable1.getValueAt(i, 1).toString());
        }
        jTextFieldTotal.setText(FormatoPesos.formato(total));
        resto = totalFactura - total;
        jTextField3.setText(FormatoPesos.formato(resto));

    }

