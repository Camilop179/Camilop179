    DefaultTableCellRenderer cellrender = new DefaultTableCellRenderer() {
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/OverriddenMethodBody

            this.setForeground((isSelected) ? new Color(0, 0, 0) : new Color(0, 0, 0));
            if (row % 2 == 0) {
                this.setBackground((isSelected) ? new Color(90, 90, 90) : new Color(102, 102, 102));
            } else {

                this.setBackground((isSelected) ? new Color(90, 90, 90) : Color.WHITE);
            }
            if (column == 1) {
                double precio = Double.parseDouble(value.toString().replace(",", ""));
                setText(FormatoPesos.formato(precio));
            }

            return this;

        }

    };
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel df = (DefaultTableModel) jTable1.getModel();

        String[] datos = new String[2];
        datos[0] = jComboBox1.getSelectedItem().toString();
        datos[1] = jTextField1.getText().trim().replace(",", "");
        df.addRow(datos);
        total();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
        if (!Validaciones.validarSuprimir(evt)) {
            int row = jTable1.getSelectedRow();
            df.removeRow(row);
        }
        total();
    }//GEN-LAST:event_jTable1KeyPressed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        formaPagoInsert formapago = new formaPagoInsert();
        FormaPago fp = new FormaPago(null, false);
        double saldo = 0;
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            formapago.insetFormaPago(Integer.parseInt(Ventas.jLabelNoVenta.getText()), jTable1.getValueAt(i, 0).toString(), Double.parseDouble(jTable1.getValueAt(i, 1).toString()));
            switch (jTable1.getValueAt(i, 0).toString()) {
                case "Efectivo":
                    fp.SumarCaja(Double.parseDouble(jTable1.getValueAt(i, 1).toString()));
                    break;
                case "Credito":
                    saldo += Double.parseDouble(jTable1.getValueAt(i, 1).toString());
                    fp.SaldoCliente(saldo);
                    break;
                case "Codigo QR":
                    fp.SumarCuentas(Double.parseDouble(jTable1.getValueAt(i, 1).toString()));
                    break;

                default:
                    throw new AssertionError();
            }
        }
        if (saldo == 0) {
            Ventas.venta(0, 0, saldo, "Cancelado");
        }else {
            Ventas.venta(0, 0, saldo, "Pendinte");
        }
        Ventas.detalleVenta();
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

