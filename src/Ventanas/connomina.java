    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        if (!jTextFieldTipo.getText().equals("")) {
            String tipo = jTextFieldTipo.getText();
            try {

                Connection cn = Conexion.Conexion();
                PreparedStatement pre = cn.prepareStatement("INSERT INTO connomina (id,concepto,descricion,valor) value(?,?,?,?)");
                pre.setInt(1, 0);
                pre.setString(2, tipo);
                pre.setString(3, jTextFieldDesc.getText().trim());
                pre.setDouble(4, Double.parseDouble(jTextFieldValor.getText().trim()));
                pre.executeUpdate();
                jTextFieldDesc.setText("");
                jTextFieldValor.setText("");
                jTextFieldTipo.setText("");
                marca();
            } catch (SQLException e) {
            }
        }
    }//GEN-LAST:event_jButtonAgregarActionPerformed

