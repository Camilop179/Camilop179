    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        Nomina nomina = Administrador.nomina;

        int l = jTable1.getSelectedRow();
        String concepto = jTable1.getValueAt(l, 1).toString();
        if (nomina.i == 1) {
            nomina.jTextFieldConceptoDeven.setText(concepto);
            nomina.buscarConNomina(concepto);
            dispose();
            nomina.i=0;
        }else if(nomina.i==2){
            nomina.jTextFieldConceptoDed.setText(concepto);
            nomina.buscarConNomina(concepto);
            dispose();
            nomina.i=0;
        }
    }//GEN-LAST:event_jTable1MouseClicked


